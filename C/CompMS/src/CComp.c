double
Vgamma(long Dim)
{
    double Pk, Pi; long i, k, d, Rh;

    k = 1; d = 2; Pk = 1.0; Pi = 3.141592653589793;

    if (Dim % 2 == 0)
    {
        for (i = 1, Rh = (Dim - 0) / 2; i <= Rh; i++)
        {
            Pk *= Pi; k *= i;
        }

        return Pk / k;
    }
    else
    {
        for (i = 1, Rh = (Dim - 1) / 2; i <= Rh; i++)
        {
            Pk *= Pi;  d *= 2; k *= 2 * i + 1;
        }

        return Pk * d / k;
    }
}
